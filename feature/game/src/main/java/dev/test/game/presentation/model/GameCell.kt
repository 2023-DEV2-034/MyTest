package dev.test.game.presentation.model

/**
 * Enum class to represent each cell on a 3x3 Tic-Tac-Toe board.
 * Each enum constant corresponds to a cell position on the board identified by a string
 * in the format "RowColumn" (e.g. "00", "01", "02").
 */
enum class GameCell {
    C00, // Row 0, Column 0
    C01,
    C02,
    C10,
    C11,
    C12,
    C20,
    C21,
    C22
}
